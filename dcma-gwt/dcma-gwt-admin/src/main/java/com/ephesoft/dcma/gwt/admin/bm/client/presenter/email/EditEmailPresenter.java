package com.ephesoft.dcma.gwt.admin.bm.client.presenter.email;

import com.ephesoft.dcma.gwt.admin.bm.client.BatchClassManagementController;
import com.ephesoft.dcma.gwt.admin.bm.client.i18n.BatchClassManagementConstants;
import com.ephesoft.dcma.gwt.admin.bm.client.i18n.BatchClassManagementMessages;
import com.ephesoft.dcma.gwt.admin.bm.client.presenter.AbstractBatchClassPresenter;
import com.ephesoft.dcma.gwt.admin.bm.client.view.email.EditEmailView;
import com.ephesoft.dcma.gwt.core.client.i18n.LocaleDictionary;
import com.ephesoft.dcma.gwt.core.client.validator.EmptyStringValidator;
import com.ephesoft.dcma.gwt.core.client.validator.NumberValidator;
import com.ephesoft.dcma.gwt.core.shared.ConfirmationDialogUtil;
import com.google.gwt.event.shared.HandlerManager;

public class EditEmailPresenter extends AbstractBatchClassPresenter<EditEmailView> {

	public EditEmailPresenter(BatchClassManagementController controller, EditEmailView view) {
		super(controller, view);
	}

	public void onCancel() {
		if (controller.isAdd()) {
			controller.getMainPresenter().showBatchClassView(controller.getBatchClass());
			controller.setAdd(false);
		} else {
			controller.getMainPresenter().getEmailViewPresenter().showEmailView();
		}
	}

	public void onSave() {
		boolean validFlag = true;
		if (controller.isAdd()) {
			if (validFlag
					&& (!view.getValidateUserNameTextBox().validate() || !view.getValidatePasswordTextBox().validate()
							|| !view.getValidateServerNameTextBox().validate() || !view.getValidateServerTypeTextBox().validate()
							|| !view.getValidateFolderNameTextBox().validate() || (view.getIsSSL() && (view.getPortNumberTextBox()
							.getValue().isEmpty() || view.getPortNumberTextBox().getValue() == null)))) {
				ConfirmationDialogUtil.showConfirmationDialog(LocaleDictionary.get().getMessageValue(
						BatchClassManagementMessages.BLANK_ERROR), LocaleDictionary.get().getConstantValue(
						BatchClassManagementConstants.ADD_EMAIL_CONFIGURATION));
				validFlag = false;
			}
			if (validFlag && view.getIsSSL() && !view.getValidatePortNumberTextBox().validate()) {
				ConfirmationDialogUtil.showConfirmationDialog(LocaleDictionary.get().getMessageValue(
						BatchClassManagementMessages.INTEGER_ERROR)
						+ " " + view.getPortNumberLabel().substring(0, view.getPortNumberLabel().length() - 1), LocaleDictionary.get()
						.getConstantValue(BatchClassManagementConstants.ADD_EMAIL_CONFIGURATION));
				validFlag = false;
			}
			if (validFlag
					&& (controller.getBatchClass().checkEmailConfiguration(view.getUsername(), view.getPassword(), view
							.getServerName(), view.getServerType(), view.getFolderName()))) {
				ConfirmationDialogUtil.showConfirmationDialog(LocaleDictionary.get().getMessageValue(
						BatchClassManagementMessages.ALREADY_EXISTS_ERROR), LocaleDictionary.get().getConstantValue(
						BatchClassManagementConstants.ADD_EMAIL_CONFIGURATION));
				validFlag = false;
			}
		} else {
			if (validFlag
					&& (!view.getValidateUserNameTextBox().validate() || !view.getValidatePasswordTextBox().validate()
							|| !view.getValidateServerNameTextBox().validate() || !view.getValidateServerTypeTextBox().validate()
							|| !view.getValidateFolderNameTextBox().validate() || (view.getIsSSL() && (view.getPortNumberTextBox()
							.getValue().isEmpty() || view.getPortNumberTextBox().getValue() == null)))) {
				ConfirmationDialogUtil.showConfirmationDialog(LocaleDictionary.get().getMessageValue(
						BatchClassManagementMessages.BLANK_ERROR), LocaleDictionary.get().getConstantValue(
						BatchClassManagementConstants.EDIT_EMAIL_CONFIGURATION_TITLE));
				validFlag = false;
			}
			if (validFlag && view.getIsSSL() && !view.getValidatePortNumberTextBox().validate()) {
				ConfirmationDialogUtil.showConfirmationDialog(LocaleDictionary.get().getMessageValue(
						BatchClassManagementMessages.INTEGER_ERROR)
						+ " " + view.getPortNumberLabel().substring(0, view.getPortNumberLabel().length() - 1), LocaleDictionary.get()
						.getConstantValue(BatchClassManagementConstants.EDIT_EMAIL_CONFIGURATION_TITLE));
				validFlag = false;
			}
		}
		if (validFlag) {
			if (controller.isAdd()) {
				controller.getBatchClass().addEmailConfiguration(controller.getSelectedEmailConfiguration());
				controller.setAdd(false);
			}
			controller.getSelectedEmailConfiguration().setUserName(view.getUsername());
			controller.getSelectedEmailConfiguration().setPassword(view.getPassword());
			controller.getSelectedEmailConfiguration().setServerName(view.getServerName());
			controller.getSelectedEmailConfiguration().setServerType(view.getServerType());
			controller.getSelectedEmailConfiguration().setFolderName(view.getFolderName());
			if (view.getPortNumber() != null && !view.getPortNumber().isEmpty()) {
				controller.getSelectedEmailConfiguration().setPortNumber(Integer.parseInt(view.getPortNumber()));
			} else {
				controller.getSelectedEmailConfiguration().setPortNumber(null);
			}
			controller.getSelectedEmailConfiguration().setIsSSL(view.getIsSSL());
			controller.getMainPresenter().getEmailViewPresenter().bind();
			controller.getMainPresenter().getEmailViewPresenter().showEmailView();
			if (controller.getSelectedEmailConfiguration().isNew()) {
				controller.getMainPresenter().getBatchClassBreadCrumbPresenter().createBreadCrumb(
						controller.getSelectedEmailConfiguration());
			}
		}
	}

	@Override
	public void bind() {
		if (controller.getSelectedEmailConfiguration() != null) {
			view.setUsername(controller.getSelectedEmailConfiguration().getUserName());
			view.setPassword(controller.getSelectedEmailConfiguration().getPassword());
			view.setServerName(controller.getSelectedEmailConfiguration().getServerName());
			view.setServerType(controller.getSelectedEmailConfiguration().getServerType());
			view.setFolderName(controller.getSelectedEmailConfiguration().getFolderName());
			Boolean isSSL = Boolean.FALSE;
			if (controller.getSelectedEmailConfiguration().getIsSSL() != null) {
				isSSL = controller.getSelectedEmailConfiguration().getIsSSL();
			}
			view.setSSL(isSSL);
			if (isSSL) {
				view.getPortNumberStar().setVisible(Boolean.valueOf(isSSL));
			}

			String portNumber = "";
			if (controller.getSelectedEmailConfiguration().getPortNumber() != null) {
				portNumber = controller.getSelectedEmailConfiguration().getPortNumber().toString();
			}
			view.setPortNumber(portNumber);

			view.getValidateUserNameTextBox().addValidator(new EmptyStringValidator(view.getUserNameTextBox()));
			view.getValidatePasswordTextBox().addValidator(new EmptyStringValidator(view.getPasswordTextBox()));
			view.getValidateServerNameTextBox().addValidator(new EmptyStringValidator(view.getServerNameTextBox()));
			view.getValidateServerTypeTextBox().addValidator(new EmptyStringValidator(view.getServerTypeTextBox()));
			view.getValidateFolderNameTextBox().addValidator(new EmptyStringValidator(view.getFolderNameTextBox()));
			view.getValidatePortNumberTextBox().addValidator(new NumberValidator(view.getPortNumberTextBox(), false, false));

			view.getValidateUserNameTextBox().toggleValidDateBox();
			view.getValidatePasswordTextBox().toggleValidDateBox();
			view.getValidateServerNameTextBox().toggleValidDateBox();
			view.getValidateServerTypeTextBox().toggleValidDateBox();
			view.getValidateFolderNameTextBox().toggleValidDateBox();
			view.getValidatePortNumberTextBox().toggleValidDateBox();
		}
	}

	@Override
	public void injectEvents(HandlerManager eventBus) {
		// Event handling is done here.
	}

}
