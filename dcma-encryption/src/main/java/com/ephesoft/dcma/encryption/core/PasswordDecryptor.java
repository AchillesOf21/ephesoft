package com.ephesoft.dcma.encryption.core;

import java.io.File;
import java.io.InputStream;
import java.util.Properties;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.core.io.ClassPathResource;

import com.ephesoft.dcma.encryption.core.PasswordDecryptor;
import com.ephesoft.dcma.encryption.exception.CryptographyException;

public class PasswordDecryptor{

	private String encryptedString;
	private String decryptedString;
	private static final String META_INF = "META-INF";
	private static final String FOLDER_NAME = "dcma-encryption";
	private static final String FILE_NAME = "dcma-encryption";
	private static final Object USE_ENCRYPTION = "password.use_encryption";
	private static final Logger log = LoggerFactory.getLogger(PasswordDecryptor.class);
	
	public PasswordDecryptor(){
		super();
	}
	
	public PasswordDecryptor(String encryptedString) {
		this.encryptedString = encryptedString;
	}

	public String decrypt(String encryptedString) throws CryptographyException {
		String decryptedString = "";
		String usingEncryption = "false";
		if (!(encryptedString == null || encryptedString.length() == 0)) {
			String filePath = META_INF + File.separator + FOLDER_NAME + File.separator + FILE_NAME + ".properties";
			try {
				InputStream propertyInStream;
				propertyInStream = new ClassPathResource(filePath).getInputStream();
				Properties properties = new Properties();
				properties.load(propertyInStream);
				usingEncryption = (String) properties.get(USE_ENCRYPTION);
			} catch (Exception e) {
				log.info("Could not find properties files. Not using encryption");
			}

			if (usingEncryption.equalsIgnoreCase(Boolean.TRUE.toString())) {
				decryptedString = EncryptorDecryptor.getEncryptorDecryptor().decryptString(encryptedString);
			} else {
				decryptedString = encryptedString;
			}
		}
		return decryptedString;
	}


	/**
	 * @return the encryptedString
	 */
	public String getEncryptedString() {
		return encryptedString;
	}

	/**
	 * @param encryptedString the encryptedString to set
	 */
	public void setEncryptedString(String encryptedString) {
		this.encryptedString = encryptedString;
	}

	
	/**
	 * @return the decryptedString
	 * @throws CryptographyException 
	 */
	public String getDecryptedString() throws CryptographyException {
		decryptedString = decrypt(encryptedString);
		return decryptedString;
	}

	
	/**
	 * @param decryptedString the decryptedString to set
	 */
	public void setDecryptedString(String decryptedString) {
		this.decryptedString = decryptedString;
	}
	

}
