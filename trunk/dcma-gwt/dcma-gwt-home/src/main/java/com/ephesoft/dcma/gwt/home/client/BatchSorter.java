package com.ephesoft.dcma.gwt.home.client;

import java.util.Collections;
import java.util.Comparator;
import java.util.LinkedHashMap;
import java.util.LinkedList;
import java.util.List;
import java.util.Map;

import com.ephesoft.dcma.gwt.core.shared.BatchInstanceDTO;

/**
 * Custom sorter for {@link Message} objects.
 * 
 * @author Ephesoft
 */
public class BatchSorter {

	/**
	 * Sort the incoming map via the comparator.
	 * 
	 * @param map the map to sort
	 * @param comparator the comparator to use
	 * @return a sorted map.
	 */
	public Map<String, BatchInstanceDTO> sort(final Map<Long, BatchInstanceDTO> map, final Comparator<BatchInstanceDTO> comparator) {
		final List<BatchInstanceDTO> list = new LinkedList<BatchInstanceDTO>(map.values());
		Collections.sort(list, comparator);
		Map<String, BatchInstanceDTO> result = new LinkedHashMap<String, BatchInstanceDTO>(list.size());
		for (BatchInstanceDTO p : list) {
			result.put(p.getBatchIdentifier(), p);
		}
		return result;
	}

	/**
	 * {@link Comparator} for sorting by {@link Message} ID.
	 */
	public static final class IdComparator implements Comparator<BatchInstanceDTO> {

		/**
		 * Used to differentiate between ascending sort and descending sort.
		 */
		private final boolean ascending;

		public IdComparator(final boolean ascending) {
			this.ascending = ascending;
		}

		@Override
		public int compare(final BatchInstanceDTO batchInstanceDTO1, final BatchInstanceDTO batchInstanceDTO2) {
			final String identifier1 = batchInstanceDTO1.getBatchIdentifier();
			final String identifier2 = batchInstanceDTO2.getBatchIdentifier();
			int returnValue;
			if (ascending) {
				returnValue = identifier1.compareTo(identifier2);
			} else {
				returnValue = identifier2.compareTo(identifier1);
			}
			return returnValue;
		}
	}

	/**
	 * {@link Comparator} for sorting by {@link Message} TEXT.
	 */
	public static final class TextComparator implements Comparator<BatchInstanceDTO> {

		/**
		 * Used to differentiate between ascending sort and descending sort.
		 */
		private final boolean ascending;

		public TextComparator(final boolean ascending) {
			this.ascending = ascending;
		}

		@Override
		public int compare(final BatchInstanceDTO batchInstanceDTO1, final BatchInstanceDTO batchInstanceDTO2) {
			final String string1 = batchInstanceDTO1.getBatchName();
			final String string2 = batchInstanceDTO2.getBatchName();
			int returnValue;
			if (ascending) {
				returnValue = string1.compareTo(string2);
			} else {
				returnValue = string2.compareTo(string1);
			}
			return returnValue;
		}
	}

	/**
	 * {@link Comparator} for sorting by {@link Message} DATE.
	 */
	public static final class DateComparator implements Comparator<BatchInstanceDTO> {

		/**
		 * Used to differentiate between ascending sort and descending sort.
		 */
		private final boolean ascending;

		public DateComparator(final boolean ascending) {
			this.ascending = ascending;
		}

		@Override
		public int compare(final BatchInstanceDTO batchInstanceDTO1, final BatchInstanceDTO batchInstanceDTO2) {
			final String dString1 = batchInstanceDTO1.getUploadedOn();
			final String dString2 = batchInstanceDTO2.getUploadedOn();
			int returnValue;
			if (ascending) {
				returnValue = dString1.compareTo(dString2);
			} else {
				returnValue = dString2.compareTo(dString1);
			}
			return returnValue;
		}
	}

	/**
	 * {@link Comparator} for sorting by {@link Message} Priority.
	 */
	public static final class PriorityComparator implements Comparator<BatchInstanceDTO> {

		/**
		 * Used to differentiate between ascending sort and descending sort.
		 */
		private final boolean ascending;

		public PriorityComparator(final boolean ascending) {
			this.ascending = ascending;
		}

		@Override
		public int compare(final BatchInstanceDTO batchInstanceDTO1, final BatchInstanceDTO batchInstanceDTO2) {
			final Integer priority1 = batchInstanceDTO1.getPriority();
			final Integer priority2 = batchInstanceDTO2.getPriority();
			int returnValue;
			if (ascending) {
				returnValue = priority1.compareTo(priority2);
			} else {
				returnValue = priority2.compareTo(priority1);
			}
			return returnValue;
		}
	}
}
