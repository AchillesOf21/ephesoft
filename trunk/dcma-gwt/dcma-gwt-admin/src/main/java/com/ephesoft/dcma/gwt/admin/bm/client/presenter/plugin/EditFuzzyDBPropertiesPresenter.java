package com.ephesoft.dcma.gwt.admin.bm.client.presenter.plugin;

import java.util.List;

import com.ephesoft.dcma.gwt.admin.bm.client.AdminConstants;
import com.ephesoft.dcma.gwt.admin.bm.client.BatchClassManagementController;
import com.ephesoft.dcma.gwt.admin.bm.client.MessageConstants;
import com.ephesoft.dcma.gwt.admin.bm.client.i18n.PluginNameConstants;
import com.ephesoft.dcma.gwt.admin.bm.client.presenter.AbstractBatchClassPresenter;
import com.ephesoft.dcma.gwt.admin.bm.client.view.plugin.EditFuzzyDBPropertiesView;
import com.ephesoft.dcma.gwt.admin.bm.client.view.plugin.EditFuzzyDBPropertiesView.EditableWidgetStorage;
import com.ephesoft.dcma.gwt.core.shared.BatchClassPluginConfigDTO;
import com.ephesoft.dcma.gwt.core.shared.BatchClassPluginDTO;
import com.ephesoft.dcma.gwt.core.shared.ConfirmationDialogUtil;
import com.google.gwt.event.dom.client.ClickEvent;
import com.google.gwt.event.dom.client.ClickHandler;
import com.google.gwt.event.shared.HandlerManager;

public class EditFuzzyDBPropertiesPresenter extends AbstractBatchClassPresenter<EditFuzzyDBPropertiesView> {

	public EditFuzzyDBPropertiesPresenter(BatchClassManagementController controller, EditFuzzyDBPropertiesView view) {
		super(controller, view);
	}

	@Override
	public void bind() {
		view.getDocFieldWidgets().clear();
		if (!(controller.getSelectedPlugin() != null && controller.getSelectedPlugin().getPlugin().getPluginName().equals(
				PluginNameConstants.FUZZYDB_PLUGIN))) {
			return;
		}
		BatchClassPluginDTO batchPluginDTO = controller.getSelectedPlugin();
		int row = 0;
		for (BatchClassPluginConfigDTO pluginConfigDTO : batchPluginDTO.getBatchClassPluginConfigs()) {
			if (!(pluginConfigDTO.getName().equalsIgnoreCase(AdminConstants.DOCUMENT_TYPE)
					|| pluginConfigDTO.getName().equalsIgnoreCase(AdminConstants.FIELD_TYPE) || pluginConfigDTO.getName()
					.equalsIgnoreCase(AdminConstants.ROW_TYPE))) {
				view.setProperties(pluginConfigDTO, row);
				row++;
			}
		}
		view.addButtons(row);
	}

	@Override
	public void injectEvents(HandlerManager eventBus) {
		view.getOkButton().addClickHandler(new ClickHandler() {

			@Override
			public void onClick(ClickEvent arg0) {
				boolean flag = false;
				List<EditableWidgetStorage> docFieldWidgets = view.getDocFieldWidgets();
				for (int index = 0; index < docFieldWidgets.size(); index++) {
					if (docFieldWidgets.get(index).isValidatable()) {
						if (!docFieldWidgets.get(index).isListBox()) {
							if (!docFieldWidgets.get(index).getTextBoxWidget().validate()) {
								ConfirmationDialogUtil.showConfirmationDialogError(MessageConstants.MANDATORY_FIELDS_ERROR_MSG);
								flag = true;
								break;
							}
						} else {
							if (docFieldWidgets.get(index).getListBoxwidget().getSelectedIndex() == -1) {
								ConfirmationDialogUtil.showConfirmationDialogError(MessageConstants.MANDATORY_FIELDS_ERROR_MSG);
								flag = true;
								break;
							}
						}
					}
				}
				if (!flag) {
					for (EditableWidgetStorage docFieldWidget : docFieldWidgets) {
						BatchClassPluginConfigDTO batchClassPluginConfigDTO = docFieldWidget.getData();
						if (docFieldWidget.isListBox()) {
							if (docFieldWidget.getListBoxwidget().isMultipleSelect()) {
								StringBuffer selectedItem = new StringBuffer("");
								Integer numberOfItemSelected = docFieldWidget.getListBoxwidget().getItemCount();
								for (int i = 0; i < numberOfItemSelected; i++) {
									if (docFieldWidget.getListBoxwidget().isItemSelected(i)) {
										selectedItem.append(docFieldWidget.getListBoxwidget().getItemText(i)).append(';');
									}
								}
								selectedItem = new StringBuffer(selectedItem.substring(0, selectedItem.length() - 1));
								batchClassPluginConfigDTO.setValue(selectedItem.toString());
							} else {
								batchClassPluginConfigDTO.setValue(docFieldWidget.getListBoxwidget().getItemText(
										docFieldWidget.getListBoxwidget().getSelectedIndex()));
							}
						} else {
							batchClassPluginConfigDTO.setValue(docFieldWidget.getTextBoxWidget().getWidget().getValue());
						}
					}
					controller.getMainPresenter().showFuzzyDBPluginView();
				}
			}
		});
		view.getCancelButton().addClickHandler(new ClickHandler() {

			@Override
			public void onClick(ClickEvent arg0) {
				controller.getMainPresenter().showFuzzyDBPluginView();
			}
		});
	}

}
