package com.ephesoft.dcma.gwt.rv.client.view;

import java.util.ArrayList;
import java.util.List;

import com.ephesoft.dcma.gwt.core.client.i18n.LocaleDictionary;
import com.ephesoft.dcma.gwt.rv.client.i18n.ReviewValidateConstants;
import com.google.gwt.core.client.GWT;
import com.google.gwt.dom.client.NativeEvent;
import com.google.gwt.event.dom.client.ClickEvent;
import com.google.gwt.event.dom.client.KeyCodes;
import com.google.gwt.uibinder.client.UiBinder;
import com.google.gwt.uibinder.client.UiField;
import com.google.gwt.uibinder.client.UiHandler;
import com.google.gwt.user.client.Event.NativePreviewEvent;
import com.google.gwt.user.client.ui.Button;
import com.google.gwt.user.client.ui.DialogBox;
import com.google.gwt.user.client.ui.HTMLPanel;
import com.google.gwt.user.client.ui.RadioButton;
import com.google.gwt.user.client.ui.VerticalPanel;

public class RadioButtonDialogView extends DialogBox {

	@UiField
	VerticalPanel radioButtonDialogView;

	@UiField
	Button okButton;

	@UiField
	Button cancelButton;

	@UiField
	VerticalPanel radioPanel;

	interface Binder extends UiBinder<HTMLPanel, RadioButtonDialogView> {
	}

	public interface DialogListener {

		public void onOkClick(int radioButtonNumber);

		public void onCancelClick();
	}

	private DialogListener listener;

	List<RadioButton> radioButtonList;

	private static final Binder binder = GWT.create(Binder.class);

	@SuppressWarnings("deprecation")
	public RadioButtonDialogView(List<String> radioButtonText, String title) {
		setWidget(binder.createAndBindUi(this));
		setAnimationEnabled(true);
		setGlassEnabled(true);
		setText(title);
		setWidth("100%");
		okButton.setText(LocaleDictionary.get().getConstantValue(ReviewValidateConstants.OK_BUTTON));
		cancelButton.setText(LocaleDictionary.get().getConstantValue(ReviewValidateConstants.CANCEL_BUTTON));
		if (null != radioButtonText && !radioButtonText.isEmpty()) {
			for (String text : radioButtonText) {
				RadioButton radioButton = new RadioButton("radioButtonGroup", text);
				if (radioButtonList == null) {
					radioButtonList = new ArrayList<RadioButton>();
				}
				radioButtonList.add(radioButton);
				radioPanel.add(radioButton);
			}
		}
		if (!radioButtonList.isEmpty()) {
			radioButtonList.get(0).setChecked(true);
		}
		radioPanel.setSpacing(5);
		radioButtonDialogView.setSpacing(5);
	}

	@Override
	protected void onPreviewNativeEvent(NativePreviewEvent preview) {
		super.onPreviewNativeEvent(preview);

		NativeEvent evt = preview.getNativeEvent();
		if (evt.getType().equals("keydown")) {
			// Use the popup's key preview hooks to close the dialog when either
			// enter or escape is pressed.
			switch (evt.getKeyCode()) {
				// case KeyCodes.KEY_ENTER:
				case KeyCodes.KEY_ESCAPE:
					hide();
					break;
			}
		}
	}

	@UiHandler("okButton")
	void onOk(ClickEvent event) {
		int radioButtonNumber = -1;
		if (radioButtonList != null) {
			int index = 0;
			for (RadioButton radioButton : radioButtonList) {
				if (radioButton.isChecked()) {
					radioButtonNumber = index;
					break;
				}
				index++;
			}
		}
		listener.onOkClick(radioButtonNumber);
		hide();
	}

	@UiHandler("cancelButton")
	void OnCancel(ClickEvent event) {
		listener.onCancelClick();
		hide();
	}

	public void addDialogListener(DialogListener dialogListener) {
		this.listener = dialogListener;
	}

	public void setRadioButtonFocus(int radioButtonNumber) {
		radioButtonList.get(radioButtonNumber).setFocus(true);
	}
}
