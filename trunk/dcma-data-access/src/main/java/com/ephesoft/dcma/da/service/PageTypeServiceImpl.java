package com.ephesoft.dcma.da.service;

import java.util.ArrayList;
import java.util.List;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;
import org.springframework.transaction.annotation.Transactional;

import com.ephesoft.dcma.core.EphesoftProperty;
import com.ephesoft.dcma.core.service.DataAccessService;
import com.ephesoft.dcma.da.dao.PageTypeDao;
import com.ephesoft.dcma.da.domain.DocumentType;
import com.ephesoft.dcma.da.domain.PageType;

/**
 * This is a database service to read data required by Page Type Service.
 * 
 * @author Ephesoft
 * @version 1.0
 * @see com.ephesoft.dcma.da.service.PageTypeService
 */
@Service
public class PageTypeServiceImpl extends DataAccessService implements PageTypeService {

	/**
	 * LOGGER to print the logging information.
	 */
	private static final Logger LOGGER = LoggerFactory.getLogger(PageTypeServiceImpl.class);

	@Autowired
	private transient PageTypeDao pageTypeDao;

	/**
	 * An api to fetch all Page types by document type name
	 * 
	 * @param docTypeName
	 * @return List<PageType>
	 */
	@Transactional(readOnly = true)
	@Override
	public List<PageType> getPageTypeByDocTypeName(String docTypeName) {
		List<PageType> pageTypes = null;
		if (null == docTypeName || "".equals(docTypeName)) {
			LOGGER.info("Document type name is null or empty.");
		} else {
			pageTypes = pageTypeDao.getPageTypeByDocTypeName(docTypeName);
		}
		return pageTypes;
	}

	/**
	 * An api to fetch all the page types for input document type ID.
	 * 
	 * @param documentTypeID String
	 * @param startResult int
	 * @param maxResult int
	 * @return List<PageType>
	 */
	@Transactional(readOnly = true)
	@Override
	public List<PageType> getPageTypes(String documentTypeID, int startResult, int maxResult) {
		List<PageType> pageTypes = null;
		if (null == documentTypeID) {
			LOGGER.info("Document type name is null.");
		} else {
			pageTypes = pageTypeDao.getPageTypes(documentTypeID, startResult, maxResult);
		}
		return pageTypes;
	}

	/**
	 * An api to fetch all Page types by batchInstanceID.
	 * 
	 * @param batchInstanceID String
	 * @return List<PageType>
	 */
	@Transactional(readOnly = true)
	@Override
	public List<PageType> getPageTypesByBatchInstanceID(String batchInstanceIdentifier) {
		List<PageType> pageTypes = null;
		if (null == batchInstanceIdentifier) {
			LOGGER.info("batchInstanceID is null.");
		} else {
			pageTypes = pageTypeDao.getPageTypesByBatchInstanceID(batchInstanceIdentifier);
		}
		return pageTypes;
	}

	/**
	 * An api to fetch all document type by Page type name.
	 * 
	 * @param name String
	 * @param batchInstanceID String
	 * @return List<DocumentType>
	 */
	@Transactional(readOnly = true)
	@Override
	public List<DocumentType> getDocTypeByPageTypeName(String name, String batchInstanceIdentifier) {
		List<DocumentType> documentType = null;
		List<PageType> pageTypes = pageTypeDao.getPageTypeByName(name, batchInstanceIdentifier);
		if (null != pageTypes) {
			documentType = new ArrayList<DocumentType>();
			for (int i = 0; i < pageTypes.size(); i++) {
				PageType pageType = pageTypes.get(i);
				DocumentType docType = pageType.getDocType();
				if (null != docType) {
					documentType.add(docType);
				}

			}
		}
		return documentType;

	}

	/**
	 * An api to fetch all batch class id, document type names and Page type names corresponding to each other.
	 * 
	 * @param batchClassIDList List<String>
	 * @return List<List<String>>
	 */
	@Transactional
	@Override
	public List<List<String>> getDocTypeNameAndPgTypeName(List<String> batchClassIDList) {
		List<Object[]> pgTyList = null;
		List<List<String>> batchIdDocPgNameList = null;
		if (null == batchClassIDList || batchClassIDList.isEmpty()) {
			LOGGER.info("Input batchClassIDList is null or empty.");
		} else {
			pgTyList = pageTypeDao.getDocTypeNameAndPgTypeName(batchClassIDList);
		}

		if (pgTyList != null) {
			batchIdDocPgNameList = new ArrayList<List<String>>();
			for (Object[] obj : pgTyList) {
				if (null != obj) {
					List<String> outStrLt = new ArrayList<String>();
					for (Object obString : obj) {
						if (null == obString) {
							outStrLt.add(null);
						} else {
							String name = String.valueOf(obString);
							if (name.equals(EphesoftProperty.UNKNOWN.getProperty())) {
								outStrLt.add(null);
							} else {
								outStrLt.add(name);
							}
						}
					}
					batchIdDocPgNameList.add(outStrLt);
				}
			}
		}

		return batchIdDocPgNameList;
	}

	/**
	 * An api to insert the page type object.
	 * 
	 * @param pageType PageType
	 */
	@Transactional
	@Override
	public void insertPageType(PageType pageType) {
		if (null == pageType) {
			LOGGER.info("PageType object is null.");
		} else {
			pageTypeDao.insertPageType(pageType);
		}
	}

	/**
	 * An api to update the pageType object.
	 * 
	 * @param pageType PageType
	 */
	@Transactional
	@Override
	public void updatePageType(PageType pageType) {
		if (null == pageType) {
			LOGGER.info("PageType object is null.");
		} else {
			pageTypeDao.updatePageType(pageType);
		}
	}

	/**
	 * An api to remove the pageType object.
	 * 
	 * @param pageType PageType
	 */
	@Transactional
	@Override
	public void removePageType(PageType pageType) {
		if (null == pageType) {
			LOGGER.info("PageType object is null.");
		} else {
			pageTypeDao.removePageType(pageType);
		}
	}
}
