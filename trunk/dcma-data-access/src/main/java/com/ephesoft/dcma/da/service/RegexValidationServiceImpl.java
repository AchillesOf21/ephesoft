package com.ephesoft.dcma.da.service;

import java.util.List;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;
import org.springframework.transaction.annotation.Transactional;

import com.ephesoft.dcma.core.service.DataAccessService;
import com.ephesoft.dcma.da.dao.RegexValidationDao;
import com.ephesoft.dcma.da.domain.FieldType;
import com.ephesoft.dcma.da.domain.RegexValidation;

/**
 * This is a database service to read data required by Regex Validation Service .
 * 
 * @author Ephesoft
 * @version 1.0
 * @see com.ephesoft.dcma.da.service.RegexValidationService
 */
@Service
public class RegexValidationServiceImpl extends DataAccessService implements RegexValidationService {

	/**
	 * LOGGER to print the logging information.
	 */
	private static final Logger LOGGER = LoggerFactory.getLogger(RegexValidationServiceImpl.class);

	@Autowired
	private RegexValidationDao regexValidationDao;

	/**
	 * An api to fetch all KV Extraction by field type.
	 * 
	 * @param fieldType FieldType
	 * @return List<RegexValidation>
	 */
	@Transactional(readOnly = true)
	@Override
	public List<RegexValidation> getRegexValidationByFieldType(FieldType fieldType) {
		List<RegexValidation> RegexValidation = null;
		if (null == fieldType) {
			LOGGER.info("Field Type object is null.");
		} else {
			RegexValidation = regexValidationDao.getRegexValidationByFieldType(fieldType);
		}
		return RegexValidation;
	}

	/**
	 * An api to insert the RegexValidation object.
	 * 
	 * @param RegexValidation RegexValidation
	 */
	@Transactional
	@Override
	public void insertRegexValidation(RegexValidation RegexValidation) {
		if (null == RegexValidation) {
			LOGGER.info("RegexValidation object is null.");
		} else {
			regexValidationDao.insertRegexValidation(RegexValidation);
		}
	}

	/**
	 * An api to update the RegexValidation object.
	 * 
	 * @param RegexValidation RegexValidation
	 */
	@Transactional
	@Override
	public void updateRegexValidation(RegexValidation RegexValidation) {
		if (null == RegexValidation) {
			LOGGER.info("RegexValidation object is null.");
		} else {
			regexValidationDao.updateRegexValidation(RegexValidation);
		}
	}

	/**
	 * An api to remove the RegexValidation object.
	 * 
	 * @param RegexValidation RegexValidation
	 */
	@Transactional
	@Override
	public void removeRegexValidation(RegexValidation RegexValidation) {
		if (null == RegexValidation) {
			LOGGER.info("RegexValidation object is null.");
		} else {
			regexValidationDao.removeRegexValidation(RegexValidation);
		}
	}
}
