package com.ephesoft.dcma.lucene;

import java.io.BufferedReader;
import java.io.File;
import java.io.IOException;
import java.io.InputStreamReader;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

import com.ephesoft.dcma.core.common.FileType;
import com.ephesoft.dcma.core.exception.DCMAApplicationException;

/**
 * This class is used to generate index for a well defined set of html(HOCR) files.
 * 
 * @author Ephesoft
 * @version 1.0
 * @see com.ephesoft.dcma.core.DCMAException.SearchClassificationService
 */
public class LearnFiles {

	/**
	 * Recostar base path.
	 */
	public static final String RECOSTAR_BASE_PATH = "RECOSTAR_PATH";
	/**
	 * Instance of Logger.
	 */
	protected static final Logger LOGGER = LoggerFactory.getLogger(LearnFiles.class);

	public static void generateHOCRFiles(final String learnFolder, final Long batchClass) throws DCMAApplicationException {
		File fBatchClass = new File(learnFolder + File.separator + batchClass);
		String[] listOfDocumetFolders = fBatchClass.list();
		if (listOfDocumetFolders != null && listOfDocumetFolders.length > 0) {
			for (String documentFolder : listOfDocumetFolders) {
				File fDocumentFolder = new File(fBatchClass.getAbsolutePath() + File.separator + documentFolder);
				String[] listOfPageFolders = fDocumentFolder.list();
				if (listOfPageFolders != null && listOfPageFolders.length > 0) {
					for (String pageFolderName : listOfPageFolders) {
						String pageFolderPath = fBatchClass.getAbsolutePath() + File.separator + documentFolder + File.separator
								+ pageFolderName;
						File fPageFolder = new File(pageFolderPath);
						String[] listOfImageFiles = fPageFolder.list();
						if (listOfImageFiles != null && listOfImageFiles.length > 0) {
							for (String eachImageFile : listOfImageFiles) {
								if (eachImageFile.contains(FileType.TIF.getExtensionWithDot())) {
									String imageAbsolutePath = pageFolderPath + File.separator + eachImageFile;
									String targetHTMlAbsolutePath = pageFolderPath + File.separator
											+ eachImageFile.substring(0, eachImageFile.lastIndexOf(".")) + FileType.HTML.getExtensionWithDot();
									
									InputStreamReader inputStreamReader = null;
									BufferedReader input = null;
									try {
										Runtime runtime = Runtime.getRuntime();
										String[] cmds = new String[3];
										cmds[0] = "cmd";
										cmds[1] = "/c";
										cmds[2] = "RecostarPlugin.exe Fpr.rsp \"" + imageAbsolutePath + "\"  \""
												+ targetHTMlAbsolutePath + "\"";
										Process process = runtime.exec(cmds, null, new File(System.getenv(RECOSTAR_BASE_PATH)));
										inputStreamReader = new InputStreamReader(process.getInputStream());
										input = new BufferedReader(inputStreamReader);
										String line = null;
										do {
											line = input.readLine();
											LOGGER.debug(line);
										} while (line != null);
										LOGGER.info("Command exited with error code no " + process.exitValue());

									} catch (Exception e) {
										LOGGER.error("Exception while generating HOCR for image" + imageAbsolutePath + e.getMessage());
										throw new DCMAApplicationException("Exception while generating HOCR for image"
												+ imageAbsolutePath + e.getMessage(), e);
									} finally {
										if (input != null) {
											try {
												input.close();
											} catch (IOException e) {
												LOGGER.error(e.getMessage(), e);
											}
										}
										if (inputStreamReader != null) {
											try {
												inputStreamReader.close();
											} catch (IOException e) {
												LOGGER.error(e.getMessage(), e);
											}
										}
									}
								} else {
									LOGGER.info("No Image file with valid extension found");
								}
							}
						} else {
							LOGGER.info("No Image file found");
						}
					}
				} else {
					LOGGER.info("No Page types present");
				}
			}
		} else {
			LOGGER.info("No Document types present");
		}
	}
}
